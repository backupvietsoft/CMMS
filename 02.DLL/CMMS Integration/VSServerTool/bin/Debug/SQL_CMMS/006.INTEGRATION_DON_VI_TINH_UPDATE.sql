CREATE PROC [dbo].[INTEGRATION_DON_VI_TINH_UPDATE]
	@STT int ,
	@DVT nvarchar(50),
	@TEN_1 nvarchar(50) ,
	@TEN_2 nvarchar(50) ,
	@TEN_3 nvarchar(100) ,
	@GHI_CHU nvarchar(255) ,
	@INSERT_DATE NVARCHAR(30) ,
	@UPDATE_DATE NVARCHAR(30) ,
	@DA_CHUYEN bit ,
	@NGAY_CHUYEN NVARCHAR(30)
AS

IF EXISTS ( SELECT DVT FROM DON_VI_TINH WHERE DVT = @DVT)
	BEGIN
		UPDATE DON_VI_TINH SET TEN_1 = @TEN_1, TEN_2 = @TEN_2, TEN_3 = @TEN_3, 
		GHI_CHU = @GHI_CHU
		WHERE DVT = @DVT 
	
	END
	ELSE
	BEGIN
		INSERT INTO DON_VI_TINH (DVT, TEN_1, TEN_2, TEN_3, GHI_CHU)
		VALUES( @DVT, @TEN_1, @TEN_2, @TEN_3, @GHI_CHU)
	END	