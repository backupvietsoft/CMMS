CREATE PROC [dbo].[INTEGRATION_DANG_XUAT_UPDATE]
	@STT int ,
	@MS_DANG_XUAT nvarchar(50),
	@DANG_XUAT_VIET nvarchar(50) ,
	@DANG_XUAT_ANH nvarchar(50) ,
	@DANG_XUAT_HOA nvarchar(100) ,
	@INSERT_DATE NVARCHAR(30) ,
	@UPDATE_DATE NVARCHAR(30) ,
	@DA_CHUYEN bit ,
	@NGAY_CHUYEN NVARCHAR(30)
AS

IF EXISTS ( SELECT MS_DANG_XUAT FROM DANG_XUAT WHERE MS_DANG_XUAT = @MS_DANG_XUAT)
	BEGIN
		UPDATE DANG_XUAT SET DANG_XUAT_VIET = @DANG_XUAT_VIET, DANG_XUAT_ANH = @DANG_XUAT_ANH, DANG_XUAT_HOA = @DANG_XUAT_HOA
		WHERE MS_DANG_XUAT = @MS_DANG_XUAT 
	
	END
	ELSE
	BEGIN
		INSERT INTO DANG_XUAT ( MS_DANG_XUAT, DANG_XUAT_VIET, DANG_XUAT_ANH, DANG_XUAT_HOA)
		VALUES(  @MS_DANG_XUAT, @DANG_XUAT_VIET, @DANG_XUAT_ANH, @DANG_XUAT_HOA)
	END	
	
	
