

CREATE PROC [dbo].[INTEGRATION_LY_DO_XUAT_KT_UPDATE]
	@MS_LY_DO_XUAT_KT nvarchar(50),
	@TEN_LY_DO_XUAT_KT nvarchar(250) 
AS
				
IF EXISTS ( SELECT * FROM LY_DO_XUAT_KT WHERE MS_LY_DO_XUAT_KT = @MS_LY_DO_XUAT_KT)
	BEGIN
		UPDATE LY_DO_XUAT_KT SET [TEN_LY_DO_XUAT_KT] = @TEN_LY_DO_XUAT_KT
		WHERE MS_LY_DO_XUAT_KT = @MS_LY_DO_XUAT_KT
	END
ELSE
	BEGIN
		INSERT INTO [LY_DO_XUAT_KT](MS_LY_DO_XUAT_KT, [TEN_LY_DO_XUAT_KT])
		VALUES (@MS_LY_DO_XUAT_KT,@TEN_LY_DO_XUAT_KT)
	END	
	
	
