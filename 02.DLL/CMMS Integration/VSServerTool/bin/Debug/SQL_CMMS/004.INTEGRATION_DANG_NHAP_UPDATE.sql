CREATE PROC [dbo].[INTEGRATION_DANG_NHAP_UPDATE]
	@STT int ,
	@MS_DANG_NHAP nvarchar(50),
	@DANG_NHAP_VIET nvarchar(50) ,
	@DANG_NHAP_ANH nvarchar(50) ,
	@DANG_NHAP_HOA nvarchar(100) ,
	@INSERT_DATE NVARCHAR(30) ,
	@UPDATE_DATE NVARCHAR(30) ,
	@DA_CHUYEN bit ,
	@NGAY_CHUYEN NVARCHAR(30)
AS

IF EXISTS ( SELECT MS_DANG_NHAP FROM DANG_NHAP WHERE MS_DANG_NHAP = @MS_DANG_NHAP)
	BEGIN
		UPDATE DANG_NHAP SET DANG_NHAP_VIET = @DANG_NHAP_VIET, DANG_NHAP_ANH = @DANG_NHAP_ANH, DANG_NHAP_HOA = @DANG_NHAP_HOA
		WHERE MS_DANG_NHAP = @MS_DANG_NHAP 
	
	END
	ELSE
	BEGIN
		INSERT INTO DANG_NHAP ( MS_DANG_NHAP, DANG_NHAP_VIET, DANG_NHAP_ANH, DANG_NHAP_HOA)
		VALUES(  @MS_DANG_NHAP, @DANG_NHAP_VIET, @DANG_NHAP_ANH, @DANG_NHAP_HOA)
	END	
	
	
